# Affichage de chaque caractères d'une chaîne avec la boucle for

mot = "Développement"
for char in mot:
    print(char)

# Affichage de chaque élemeent d'une liste avec la boucle for

liste = ["Element N°1", "Element N°2", "Element N°3", "Element N°4", "Element N°5"]
for element in liste:
    print(element)